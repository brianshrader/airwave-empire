#!/usr/bin/env node
/**
 * Bundles formatLifecycle profile runtime → src/formatLifecycleProfileRuntime.iife.js
 * Exposes globalThis.__wlProfileCountryLifecycleMktFmtMult for legacy.js and Node VMs.
 */
import { execFileSync } from 'child_process';
import { mkdirSync, readFileSync, unlinkSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const entry = path.join(root, 'src', 'formatLifecycleProfileRuntime.entry.mjs');
const outfile = path.join(root, 'src', 'formatLifecycleProfileRuntime.iife.js');
const tmp = path.join(root, 'tmp', '_format_lifecycle_iife_tmp.js');

mkdirSync(path.dirname(tmp), { recursive: true });

execFileSync(
  'npx',
  [
    '--yes',
    'esbuild',
    entry,
    '--bundle',
    '--format=iife',
    '--global-name=WLFL',
    `--outfile=${tmp}`,
  ],
  { stdio: 'inherit', cwd: root },
);

const mid = readFileSync(tmp, 'utf8');
unlinkSync(tmp);

const header =
  '/* Auto-generated by scripts/build-format-lifecycle-iife.mjs — do not edit by hand. */\n';
const footer = `
;(function(){
  var g=typeof globalThis!=='undefined'?globalThis:typeof window!=='undefined'?window:{};
  g.__wlProfileCountryLifecycleMktFmtMult=WLFL.profileCountryLifecycleMktFmtMult;
})();
`;

writeFileSync(outfile, header + mid + footer, 'utf8');
if (!readFileSync(outfile, 'utf8').includes('__wlProfileCountryLifecycleMktFmtMult')) {
  throw new Error('IIFE output missing global hook');
}
console.log(`Wrote ${path.relative(root, outfile)}`);
