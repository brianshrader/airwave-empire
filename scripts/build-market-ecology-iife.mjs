#!/usr/bin/env node
/**
 * Bundles `src/marketEcologyCore.js` → `src/marketEcologyCore.iife.js`
 * Exposes `globalThis.__wlDeriveMarketEcology` and `globalThis.__wlModernChrPressure01FromEcology`
 * for classic `legacy.js` and Node VMs.
 */
import { execFileSync } from 'child_process';
import { mkdirSync, readFileSync, unlinkSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const entry = path.join(root, 'src', 'marketEcologyCore.js');
const outfile = path.join(root, 'src', 'marketEcologyCore.iife.js');
const tmp = path.join(root, 'tmp', '_market_ecology_iife_tmp.js');

mkdirSync(path.dirname(tmp), { recursive: true });

execFileSync(
  'npx',
  [
    '--yes',
    'esbuild',
    entry,
    '--bundle',
    '--format=iife',
    '--global-name=WLME',
    `--outfile=${tmp}`,
  ],
  { stdio: 'inherit', cwd: root }
);

const mid = readFileSync(tmp, 'utf8');
unlinkSync(tmp);

const header =
  '/* Auto-generated by scripts/build-market-ecology-iife.mjs — do not edit by hand. */\n';
const footer = `
;(function(){
  var g=typeof globalThis!=='undefined'?globalThis:typeof window!=='undefined'?window:{};
  g.__wlDeriveMarketEcology=WLME.deriveMarketEcology;
  g.__wlModernChrPressure01FromEcology=WLME.modernChrPressure01FromEcology;
  g.__wlExpectedChrBucketStrengthByEra=WLME.expectedChrBucketStrengthByEra;
  g.__wlExpectedChrLeaderShareCap=WLME.expectedChrLeaderShareCap;
  g.__wlChrLineageBucketAppealEraMultFromEcology=WLME.chrLineageBucketAppealEraMultFromEcology;
})();
`;

writeFileSync(outfile, header + mid + footer, 'utf8');
if (!readFileSync(outfile, 'utf8').includes('__wlDeriveMarketEcology')) {
  throw new Error('IIFE output missing global hook');
}
console.log(`Wrote ${path.relative(root, outfile)}`);
